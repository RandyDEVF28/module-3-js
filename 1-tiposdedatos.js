// Valores proporcionados
let numero1 = 42; // Number
let texto = 'Veinticinco'; // String
let numero2 = -666; // Number
let booleano1 = true; // Boolean
let numero3 = 0; // Number
let textoVacio = ''; // String vacío
let nulo = null; // Null
let indefinido; // Undefined
let booleano2 = false; // Boolean

// Imprimir los valores y sus tipos
console.log("Número 1:", numero1, typeof numero1);
console.log("Texto:", texto, typeof texto);
console.log("Número 2:", numero2, typeof numero2);
console.log("Booleano 1:", booleano1, typeof booleano1);
console.log("Número 3:", numero3, typeof numero3);
console.log("Texto vacío:", textoVacio, typeof textoVacio);
console.log("Nulo:", nulo, typeof nulo); // typeof null devuelve 'object' (quirk de JS)
console.log("Indefinido:", indefinido, typeof indefinido);
console.log("Booleano 2:", booleano2, typeof booleano2);